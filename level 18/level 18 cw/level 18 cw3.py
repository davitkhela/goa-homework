# დაწერეთ პროგრამა სადაც შეამოწმეთ, სტუდენტმა ჩააბარა თუ არა ჩააბარა კურსი მათი გამოცდის ქულების მიხედვით. სთხოვეთ მომხმარებელს შეიყვანოს ქულები შუალედური გამოცდისთვის, დასკვნითი გამოცდისთვის და პროექტისთვის. დარწმუნდით, რომ მომხმარებელმა შეიყვანოს სწორი ქულები (პოზიტიური რიცხვები 0-დან 100-მდე) თითოეული კომპონენტისთვის. გამოიყენეთ შემდეგი შეფასების კრიტერიუმები: თუ საშუალო ქულა (20% შუალედური კურსისთვის, 40% საბოლოო და 40% პროექტისთვის) არის 70 ან მეტი, სტუდენტი გაივლის კურსს. თუ საშუალო ქულა 70-ზე დაბალია, სტუდენტი ვერ ახერხებს კურსის ჩაბარებას. აჩვენეთ მომხმარებლისთვის შეტყობინება, რომელშიც მითითებულია მისი გავლის/ჩავარდნის სტატუსი და გამოთვლილი საშუალო ქულა.
def check_pass_fail():
    midterm = float(input("Midterm exam score (0-100): "))
    final_exam = float(input("Final exam score (0-100): "))
    project = float(input("Project score (0-100): "))

    if 0 <= midterm <= 100 and 0 <= final_exam <= 100 and 0 <= project <= 100:
        average = (0.2 * midterm) + (0.4 * final_exam) + (0.4 * project)
        if average >= 70:
            print(f"Passed the course. Average score: {average:.2f}")
        else:
            print(f"Failed the course. Average score: {average:.2f}")
    else:
        print("Please enter valid scores (0-100).")

check_pass_fail()
