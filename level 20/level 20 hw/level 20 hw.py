# შექმენი სია სახელწოდებით numbers, რომელიც შეიცავს 5 რიცხვს.
numbers = [1,2,3,4,5]

# სიის ბოლოს დაამატეთ ორი ახალი რიცხვი
numbers = numbers + [6,7]

# შეცვალე სიის მესამე ელემენტი ნებისმიერი სხვა რიცხვით.
